    private void GenerateBillByNumberBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GenerateBillByNumberBtnActionPerformed
        String ByBillNumber = BoxByBillNumber.getText();
        if (!(ByBillNumber.trim().isEmpty())) {
            try {
                DB_CONNECTION db = new DB_CONNECTION();
                db.Pstmt = db.Con.prepareStatement("select invoice.med_name,stock_detail.med_mfd,stock_detail.med_exd,invoice.med_price, invoice.med_qty, invoice.cust_name, invoice.cust_contact, invoice.invc_date_time from invoice, stock_detail where invoice.med_name=stock_detail.med_name and invoice.invoice_id=?");
                db.Pstmt.setInt(1, Integer.parseInt(ByBillNumber));
                db.Rst = db.Pstmt.executeQuery();
                if (db.Rst.isBeforeFirst()) {
                    int am = 0;
                    Vector<String> header = new Vector<String>();
                    header.add("Medicine Name");
                    header.add("Mfd. Date");
                    header.add("Exp. Date");
                    header.add("Price/Unit");
                    header.add("Quantity");
                    header.add("Amount");
                    Vector<Vector<String>> data = new Vector<Vector<String>>();
                    while (db.Rst.next()) {
                        Vector<String> temp = new Vector<String>();
                        temp.add(db.Rst.getString(1));
                        temp.add(db.Rst.getString(2));
                        temp.add(db.Rst.getString(3));
                        temp.add(db.Rst.getString(4));
                        temp.add(db.Rst.getString(5));
                        int amt = Integer.parseInt(db.Rst.getString(4)) * Integer.parseInt(db.Rst.getString(5));
                        am += amt;
                        INInvoiceNumber.setText(ByBillNumber);
                        INCustomerName.setText(db.Rst.getString(6));
                        INContactNumber.setText(db.Rst.getString(7));
                        INInvoiceDate.setText(db.Rst.getString(8));
                        INPayableAmount.setText(String.valueOf(am));
                        INReturnAmount.setText("0");
                        INRecieveedAmount.setText(String.valueOf(am));
                        temp.add(String.valueOf(amt));
                        data.add(temp);
                    }
                    InvoiceTable.setModel(new DefaultTableModel(data, header));
                    GenerateBillByNumberFrame.setVisible(false);
                    InvoiceBill.setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(this, "Wrong Bill Number!  Plese Enter Correct Bill Number");
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please Fill The Bill Number");
        }
    }//GEN-LAST:event_GenerateBillByNumberBtnActionPerformed

